
ALTER TABLE T_PAAS_DESTORY_OBJ  DROP PRIMARY KEY CASCADE;

DROP TABLE T_PAAS_DESTORY_OBJ CASCADE CONSTRAINTS;

CREATE TABLE T_PAAS_DESTORY_OBJ 
(
	T_PASSDESTORYOBJ_ID      NUMBER						    NOT NULL PRIMARY KEY,
	OBJNAME 				 VARCHAR2(200)				     NULL,
	OBJTYPE					 INT						     NULL,
	COMMAND					 VARCHAR2(200)				    NOT NULL,
	OBJID					 VARCHAR2(200)				     NULL,
  PARAMSTR       VARCHAR2(200)             NULL,
	EXE_RESULT				 INT							 NULL,
  FAILREASON         VARCHAR2(2000)    NULL,
	CREATED_BY               VARCHAR(60)                    NOT NULL,
	CREATED_ON               TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
	MODIFIED_BY              VARCHAR(60)                    NULL,
	MODIFIED_ON              TIMESTAMP DEFAULT SYSTIMESTAMP NULL
);
COMMENT ON COLUMN T_PAAS_DESTORY_OBJ.OBJNAME
  is 'cloudstack名称';
COMMENT ON COLUMN T_PAAS_DESTORY_OBJ.OBJTYPE
  is 'cloudstack对象类型。1：项目，2：VM';
COMMENT ON COLUMN T_PAAS_DESTORY_OBJ.COMMAND
  is '需要执行的命令字';
COMMENT ON COLUMN T_PAAS_DESTORY_OBJ.OBJID
  is 'cloudstack中对象ID';
COMMENT ON COLUMN T_PAAS_DESTORY_OBJ.EXE_RESULT
  is '执行结果。1：失败，2：成功。';
COMMENT ON COLUMN T_PAAS_DESTORY_OBJ.PARAMSTR
IS '命令参数';
COMMENT ON COLUMN  T_PAAS_DESTORY_OBJ.FAILREASON
  is '失败原因';
