package com.cmsz.cloudplatform.dto;

public class VCpu {
	private  int numberVcpus;
	private  int minVcpus;
	private  int maxVcpus;
	public int getNumberVcpus() {
		return numberVcpus;
	}
	public void setNumberVcpus(int numberVcpus) {
		this.numberVcpus = numberVcpus;
	}
	public int getMinVcpus() {
		return minVcpus;
	}
	public void setMinVcpus(int minVcpus) {
		this.minVcpus = minVcpus;
	}
	public int getMaxVcpus() {
		return maxVcpus;
	}
	public void setMaxVcpus(int maxVcpus) {
		this.maxVcpus = maxVcpus;
	}
	
	
}
